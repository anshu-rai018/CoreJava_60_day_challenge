// Java Program to Display Alphabets (A to Z) using a loop:
// https://www.codeswithpankaj.com/
// https://www.p4n.in/

public class DisplayAlphabets {
    public static void main(String[] args) {
        char ch;
        for(ch = 'A'; ch <= 'Z'; ch++) {
            System.out.print(ch + " ");
        }
    }
}


