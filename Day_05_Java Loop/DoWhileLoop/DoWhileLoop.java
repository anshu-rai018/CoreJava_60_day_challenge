public class DoWhileLoop{

            // 1 . initialization
            // 2 . Code to be executed in each iteration
            // 3 . iteration
            // 4 . condition
            
            

            public static void main(String[] args) {
                int a = 1;

                do{
                    
                    System.out.println("a = "+a);
                    a++;

                }while(a<=10);
            }


    }
